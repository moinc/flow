package nl.agiletech.flow.cmp.project;

@SuppressWarnings("serial")
public class PlatformResolverException extends Exception {
	public PlatformResolverException() {
		super();
	}

	public PlatformResolverException(String message, Throwable cause) {
		super(message, cause);
	}

	public PlatformResolverException(String message) {
		super(message);
	}

	public PlatformResolverException(Throwable cause) {
		super(cause);
	}
}
