package nl.agiletech.flow.cmp.jarinspector;

@SuppressWarnings("serial")
public class ClassUtilException extends RuntimeException {
	public ClassUtilException() {
		super();
	}

	public ClassUtilException(String message, Throwable cause) {
		super(message, cause);
	}

	public ClassUtilException(String message) {
		super(message);
	}

	public ClassUtilException(Throwable cause) {
		super(cause);
	}
}
