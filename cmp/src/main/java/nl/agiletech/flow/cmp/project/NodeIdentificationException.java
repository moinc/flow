package nl.agiletech.flow.cmp.project;

@SuppressWarnings("serial")
public class NodeIdentificationException extends Exception {
	public NodeIdentificationException() {
		super();
	}

	public NodeIdentificationException(String message, Throwable cause) {
		super(message, cause);
	}

	public NodeIdentificationException(String message) {
		super(message);
	}

	public NodeIdentificationException(Throwable cause) {
		super(cause);
	}
}
